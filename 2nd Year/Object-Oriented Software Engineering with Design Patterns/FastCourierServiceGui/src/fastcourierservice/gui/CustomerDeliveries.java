    public CustomerDeliveries(FastCourierServiceGUI parent, boolean modal,
            CustomerDeliveriesType function) {
        this(parent, modal);
        windowType = function;
        this.parent = parent;
        switch (this.windowType) {
            case COMPLETED:
                lblHeading.setText("Completed Customer Deliveries");
                this.btnSetDelivered.setVisible(false);
                this.btnUpdateStatus.setVisible(false);
                break;
            case INCOMPLETE:
                lblHeading.setText("Incomplete Customer Deliveries");
                break;
        }

        this.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent ev) {
                onClose();
            }
        });
    }

    private void onClose() {
        this.customers.removeObserver(this);
    }

