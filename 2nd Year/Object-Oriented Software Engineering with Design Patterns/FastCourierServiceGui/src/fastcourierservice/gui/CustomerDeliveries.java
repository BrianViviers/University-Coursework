    public void setEditableCustomer(int customerIndex) {
        this.customerToEdit = this.customers.getCustomerAt(customerIndex);
        this.lblCustomerName.setText(this.customerToEdit.getFullName());
        rebuildComboBox();
        this.update();
    }

    @Override
    public void update() {
        if (null != this.customers) {
            rebuildComboBox();
            showDeliveryDetails();
        }
    }

    public void setCustomers(CustomerList customers) {
        if (null != this.customers) {
            this.customers.removeObserver(this);
            this.customers = null;
        }
        this.customers = customers;
        if (null != this.customers) {
            this.customers.registerObserver(this);
        }
    }

    private void rebuildComboBox() {
        model = new DefaultComboBoxModel<>();
        Object selectedItemInOldBox = cbxDeliveries.getSelectedItem();
        if (null != customerToEdit) {
            switch (windowType) {
                case INCOMPLETE:
                    for (int i = 0; i < customerToEdit.getNoOfDeliveries(); i++) {
                        Delivery tempDelivery = customerToEdit.getDeliveryAt(i);
                        if (customerToEdit.getDeliveryAt(i) != null) {
                            if (tempDelivery.getStatus().equals(DeliveryStatus.PENDINGCOLLECTION)
                                    || tempDelivery.getStatus().equals(DeliveryStatus.INTRANSIT)
                                    || tempDelivery.getStatus().equals(DeliveryStatus.DELIVERYREFUSED)) {

                                model.addElement(tempDelivery);
                            }
                        }
                    }
                    break;
                case COMPLETED:
                    for (int i = 0; i < customerToEdit.getNoOfDeliveries(); i++) {
                        Delivery tempDelivery = customerToEdit.getDeliveryAt(i);
                        if (customerToEdit.getDeliveryAt(i) != null) {
                            if (tempDelivery.getStatus().equals(DeliveryStatus.UNDELIVERABLE)
                                    || tempDelivery.getStatus().equals(DeliveryStatus.DELIVERED)) {

                                model.addElement(tempDelivery);
                            }
                        }
                    }
                    break;
            }
        }
        cbxDeliveries.setModel(model);
        if (model.getSize()
                > 0) {
            if (null != selectedItemInOldBox) {
                cbxDeliveries.setSelectedItem(selectedItemInOldBox);
            } else {
                cbxDeliveries.setSelectedIndex(0);
            }
            showDeliveryDetails();
        } else {
            this.dispose();
        }
    }

    private void showDeliveryDetails() {
        DateFormat df = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
        DecimalFormat decF = new DecimalFormat("0.00");
        if (null != customerToEdit) {
            Delivery selectedDelivery = model.getElementAt(cbxDeliveries.getSelectedIndex());
            if (null != selectedDelivery) {
                StringBuilder resultBuilder = new StringBuilder();
                resultBuilder.append(selectedDelivery.getColAddress().getAddressLine1());
                resultBuilder.append("\n");
                resultBuilder.append(selectedDelivery.getColAddress().getAddressLine2());
                resultBuilder.append("\n");
                resultBuilder.append(selectedDelivery.getColAddress().getCity());
                resultBuilder.append("\n");
                resultBuilder.append(selectedDelivery.getColAddress().getPostcode());
                collectionAddressDisplay.setText(resultBuilder.toString());

                resultBuilder = new StringBuilder();
                resultBuilder.append(selectedDelivery.getDelAddress().getAddressLine1());
                resultBuilder.append("\n");
                resultBuilder.append(selectedDelivery.getDelAddress().getAddressLine2());
                resultBuilder.append("\n");
                resultBuilder.append(selectedDelivery.getDelAddress().getCity());
                resultBuilder.append("\n");
                resultBuilder.append(selectedDelivery.getDelAddress().getPostcode());
                deliveryAddressDisplay.setText(resultBuilder.toString());

                txtCostPerKg.setText("£" + decF.format(
                        selectedDelivery.getCostPerKg()));
                txtTotalCost.setText("£" + decF.format(
                        selectedDelivery.getTotalCost()));
                txtWeight.setText(Double.toString(
                        selectedDelivery.getWeight()));
                txtNoOfPackages.setText(
                        Integer.toString(selectedDelivery.getNoOfPackages()));
                txtStartDate.setText(df.format(
                        selectedDelivery.getStartDate()));
                Date deliveredDate = selectedDelivery.getDeliveredDate();
                if (null != deliveredDate) {
                    txtDeliveredDate.setText(df.format(deliveredDate));
                } else {
                    txtDeliveredDate.setText("");
                }
                
                lblStatusDisplayed.setText(selectedDelivery.getStatus().toString());
                notesDisplay.setText(selectedDelivery.getNotes());
            }
        }
    }
