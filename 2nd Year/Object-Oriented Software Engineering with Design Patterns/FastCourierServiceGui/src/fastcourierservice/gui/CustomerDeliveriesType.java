package fastcourierservice.gui;

/**
 * Enumeration declaring the possible windows for viewing customer deliveries.
 * @author Brian Viviers
 */
public enum CustomerDeliveriesType {
    
    /**
     * Used to declare the type of customer deliveries 
     * window and what functions will be available for all
     * completed deliveries.
     */
    COMPLETED,
    /**
     * Used to declare the type of customer deliveries 
     * window and what functions will be available for all
     * incomplete deliveries.
     */
    INCOMPLETE;
    
}
