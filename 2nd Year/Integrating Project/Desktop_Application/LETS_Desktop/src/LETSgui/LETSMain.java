    private void combineData() {
        for (int j = 0; j < memberList.memberListSize(); j++) {
            for (int i = 0; i < advertList.totalAmountOfAdverts(); i++) {

                if (this.memberList.getMemberAt(j).getMemberID() == this.advertList.getAdvertAtIndex(i).getMemberID()) {
                    Advert newAd = this.advertList.getAdvertAtIndex(i);
                    this.memberList.getMemberAt(j).addAdvert(newAd);
                }
            }
        }
    }

