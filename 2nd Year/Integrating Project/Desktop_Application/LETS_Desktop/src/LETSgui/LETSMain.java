    /*Thread for loading a new image.*******************************************/
    private void loadImageAdvert(Advert advery, JLabel image) {
        final Advert ad = advery;
        final JLabel icon = image;
        final SetImage si = new SetImage();
        new Thread() {
            @Override
            public void run() {
                SQLStatements sql = new SQLStatements();
                try {
                    sql.createConnection();
                } catch (SQLException ex) {
                    Logger.getLogger(LETSMain.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(LETSMain.class.getName()).log(Level.SEVERE, null, ex);
                }
                Blob blobImage = ad.getImage();
                si.setImageFromBlob(blobImage, icon);
                try {
                    sql.closeConnection();
                } catch (SQLException ex) {
                    Logger.getLogger(LETSMain.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(LETSMain.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }.start();
    }

