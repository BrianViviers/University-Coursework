    /*Method used to reset a members password**********************************/
    private void createPassword(String password) {
        String hashedPassword = BCrypt.hashpw(password, BCrypt.gensalt(12));
        int selectedPane = this.tbPaneRules.getSelectedIndex();
        Member currentMember = new Member();

        if (selectedPane == 0) {
            currentMember = selectedMember();

        } else if (selectedPane == 1) {
            currentMember = selectedMemberAdvertPane();

        }

        String email = currentMember.getEmail();

        SQLStatements sql = new SQLStatements();

        try {
            sql.createConnection();
            sql.resetPassword(hashedPassword, email);
            sql.closeConnection();
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(LETSMain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
